// ถังน้ำนใบหนึ่งบรรจุ 5,832 ลิตร ถ้ามีการนําน˺าทีˠมีอยู่ในถัง ไปใช้ทุกวันตลอดสัปดาห์
// โดยทˠีแต่ละวันจะนําน˺าไปใช้หนˠึงในสามของปริมาณน˺าในถัง
// อยากทราบว่าเมˠือครบวันจะมีน˺าเหลืออยู่ในถังกีˠลิตร
// ให้เขียนฟังก์ชัน y = Calculate(x) โดยใช้ภาษาใดก็ได้ เพื่อแก้ป˞ญหา
function Calculate(x) {
    for (let i = 0; i < 7; i++) {
        x = x - (x / 3);
    }
    return x;
}

let initialAmount = 5832;
let remainingAmount = Calculate(initialAmount);

console.log("ปริมาณน้ำที่เหลือหลังครบ 7 วัน:", remainingAmount.toFixed(2), "ลิตร");


